    public static function parseFunctionCallByCode(string $code): ?array
    {
        $tokens = token_get_all('<?php ' . $code);
        $functionName = null;
        $arguments = [];
        $inFunctionCall = false;
        $parenthesisCount = 0;

        foreach ($tokens as $token) {
            if (is_array($token)) {
                if (in_array($token[0], [T_STRING, T_EMPTY]) && ! $inFunctionCall) {
                    $functionName = $token[1];
                    $inFunctionCall = true;
                } elseif ($inFunctionCall && ($token[0] === T_CONSTANT_ENCAPSED_STRING || $token[0] === T_VARIABLE || $token[0] === T_LNUMBER)) {
                    $arguments[] = trim($token[1], '\'');
                }
            } else {
                if ($token === '(') {
                    ++$parenthesisCount;
                } elseif ($token === ')') {
                    --$parenthesisCount;
                    if ($parenthesisCount === 0) {
                        break;
                    }
                }
            }
        }

        if ($functionName) {
            return [
                'function' => $functionName,
                'arguments' => $arguments,
            ];
        }
        return null;
    }

    public static function unCamelize(string $camelCaps, string $separator = '_'): string
    {
        if (empty($camelCaps)) {
            return '';
        }
        return strtolower(preg_replace('/([a-z])([A-Z])/', '$1' . $separator . '$2', $camelCaps));
    }

    public static function unFlattenArray(array $array): array
    {
        $result = [];
        foreach ($array as $key => $value) {
            $keys = explode('.', (string) $key);
            $ref = &$result;
            foreach ($keys as $subKey) {
                $ref = &$ref[$subKey];
            }
            $ref = $value;
        }
        return $result;
    }

    public static function flattenArray(array $array, string $prefix = ''): array
    {
        $result = [];
        foreach ($array as $key => $value) {
            $newKey = ($prefix ? $prefix . '.' : '') . $key;
            if (is_array($value)) {
                $result = array_merge($result, self::flattenArray($value, $newKey));
            } else {
                $result[$newKey] = $value;
            }
        }
        return $result;
    }

    public static function calculateElapsedTime(float $requestTime, float $responseTime): float
    {
        return round(($responseTime - $requestTime) * 1000, 2);
    }

    public static function formatMicroTime(float|string $microTime): string
    {
        if (is_string($microTime)) {
            $microTime = explode(' ', $microTime);
            $integerTimestamp = (int) $microTime[1];
            $microseconds = $microTime[0] * 1000000;
        } else {
            $integerTimestamp = (int) $microTime;
            $microseconds = ($microTime - $integerTimestamp) * 1000000; // Microsecond portion
        }
        $dateTime = date('Y-m-d H:i:s', $integerTimestamp);
        return "{$dateTime}.{$microseconds}";
    }

